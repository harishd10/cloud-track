package vgl.iisc.utils;

public class Triple <T> {
	
	public T x;
	public T y;
	public T z;
	
	public Triple() {
	}
	
	public Triple(T xx, T yy, T zz) {
		x = xx;
		y = yy;
		z = zz;
	}
}
